          SwitchListTile(
            value: _isLactoseFree,
            onChanged: (newValue) {
              setState(() {
                _isLactoseFree = newValue;
              });
            },
            title: Text(
              "Lactose-free",
              style: Theme.of(context)
                  .textTheme
